// Expected Header: ID;SKU;Ürün Adı;Kategori;Miktar;Konum;Satış Fiyatı;Alış Fiyatı;Para Birimi;Son Güncelleme
const header = "ID;SKU;Ürün Adı;Kategori;Miktar;Konum;Satış Fiyatı;Alış Fiyatı;Para Birimi;Son Güncelleme";

const rows = rawData.split('\n').filter(line => line.trim()).map(line => {
    // Regex matches:
    // 1: SKU (starts with digit or word chars for tangfix)
    // 2: Name
    // 3: Category
    // 4: Price (remove ₺)
    // 5: Quantity (remove adet)

    // Using simple splitting by whitespace might fail if name has spaces.
    // The user data appears to be tab separated in the prompt display, or fixed width.
    // Let's assume tabs or at least 2 spaces separator.

    // Split by tab or multiple spaces
    const parts = line.split(/\t+/);

    if (parts.length < 5) return null;

    const sku = parts[0].trim();
    const name = parts[1].trim();
    const category = parts[2].trim();
    let priceStr = parts[3].trim().replace('₺', '').replace(',', '.'); // Assuming dot decimal because output in prompt is 100.00
    // Wait, prompt says ₺100.00 which is dot.

    // Stock "13 adet"
    const quantityStr = parts[4].trim().replace(' adet', '').trim();

    // Calculate cost (mock 60% of price)
    const price = parseFloat(priceStr);
    const cost = (price * 0.6).toFixed(2);

    // Location default
    const location = "Depo";
    const now = new Date().toISOString();

    return `;${sku};${name};${category};${quantityStr};${location};${price};${cost};TL;${now}`;
}).filter(r => r !== null);

const csvContent = "\uFEFF" + header + "\n" + rows.join("\n");
fs.writeFileSync('urunler.csv', csvContent, 'utf8');
